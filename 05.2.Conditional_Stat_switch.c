#include <stdio.h>

int main(void)
{
	int rank = 3, m = 0;

	switch (rank)
	{
	case 1:
		m = 100;
		break;
	case 2:
		m = 200;
		break;
	case 3:
		m = 300;
		break;//생략 가능
	default:
		m = -1;
		break;
	}

	printf("m = %d\n", m);

	/*
	사칙 연산 입력(정수) : 
		11 / 3
		11 / 3 = 3
	*/

	return 0;

}
//
//값을 입력받은 두 변수의 비교
//0~100사이의 정수를 입력받아 학점 출력
//입력받은 정수의 홀수 / 짝수
